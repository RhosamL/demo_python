import math
radius = 7
area = math.pi * (radius ** 2)
pi = math.pi
print(f"the value of pi is {pi:.5f}")
print(f"\narea of a circle is {area}")
#using some math functions
round_up = math.ceil (area)
lower = math.floor (pi)
print(f"first round up of area {round_up}")
print(f"then lowering pi: {lower}")
